package Lab4.task3;

public class Task3 {
    public static int multiply(int a, int b) {
        return a * b;
    }

    public static void main(String[] args) {
        System.out.println(multiply(10, 2));
        System.out.println(multiply(7, 9));
        System.out.println(multiply(4, 8));
    }
}
